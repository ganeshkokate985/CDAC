/*5.
 * Write a program to display ASCII values for character constants ‘\n’, ‘\r’, ‘\t’, ‘\a’, ‘\b’.
 */

#include<stdio.h>
void main()
{
	printf("ASCII value for \\n = %d \n",'\n');
	printf("ASCII value for \\r = %d \n",'\r');
	printf("ASCII value for \\t = %d \n",'\t');
	printf("ASCII value for \\a = %d \n",'\a');
	printf("ASCII value for \\b = %d \n",'\b');

}
